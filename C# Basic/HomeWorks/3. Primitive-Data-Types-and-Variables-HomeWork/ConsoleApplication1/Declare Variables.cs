using System;

namespace ConsoleApplication1
{
    class DeclareVariables 
    {
        static void Main()
        {
            ushort var1 = 52130;
            Console.WriteLine("for number   52130 we use ushort: " + var1);
            sbyte var2 = -115;
            Console.WriteLine("for number    -115 we use sbyte:  " + var2);
            int var3 = 4825932;
            Console.WriteLine("for number 4825932 we use int:    " + var3);
            sbyte var4 = 97;
            Console.WriteLine("for number      97 we use sbyte:  " + var4);
            short var5 = -10000;
            Console.WriteLine("for number  -10000 we use short:  " + var5);
        }
    }
}
